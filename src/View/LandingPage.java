    public Employee employeeFactory(String type, String firstName, String lastName, String ssn, int age, int pay) {
        Employee emp = null;

        if (type == "Professor") {
            emp = new Professor(type, firstName, lastName, ssn, age, pay);
        } else if (type == "AssociateProfessor") {
            emp = new AssociateProfessor(type, firstName, lastName, ssn, age, pay);
        } else if (type == "AssistantProfessor") {
            emp = new AssistantProfessor(type, firstName, lastName, ssn, age, pay);
        } else if (type == "Instructor") {
            emp = new Instructor(type, firstName, lastName, ssn, age, pay);
        } else if (type == "SalariedStaff") {
            emp = new SalariedStaff(type, firstName, lastName, ssn, age, pay);
        } else if (type == "HourlyStaff") {
            emp = new HourlyStaff(type, firstName, lastName, ssn, age, pay);
        }
        return emp;
    }

