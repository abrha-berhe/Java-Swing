    public Object getFieldValueOfEmployee(Employee emp, int col) {
        Object temp = null;
        if (col == 0) {
            temp = emp.getType();
        } else if (col == 1) {
            temp = emp.getFirstName();
        } else if (col == 2) {
            temp = emp.getLastName();
        }
        if (col == 3) {
            temp = emp.getSsn();
        } else if (col == 4) {
            temp = emp.getSsn();
        } else if (col == 5) {
            temp = emp.getAnnualPay();
        }
        return temp;
    }
